import java.util.*;
class AllInputs{
public static void main(String[] args){
Scanner sc=new Scanner(System.in);
System.out.println("enter the string");
String a=sc.next();
System.out.println("enter the long number");
long bcd=sc.nextLong();
System.out.println("enter the double");
double e=sc.nextDouble();
System.out.println("enter the float");
float f=sc.nextFloat();
System.out.println("enter the character");
char g=sc.next().charAt(0);
System.out.println("enter the byte");
byte h=sc.nextByte();
System.out.println("enter the boolean");
boolean i=sc.nextBoolean();
System.out.println("the scanned values are:\nstring:"+a+"\nlong :"+bcd+"\ndouble :"+e+"\nfloat :"+f+"\nchar :"+g+"\nbyte :"+h+"\nboolean :"+i);
}
}





